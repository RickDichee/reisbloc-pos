import React from 'react'
import ReactDOM from 'react-dom/client'
import App from './App'
import './styles/globals.css'

// Registrar Service Worker para PWA y soporte offline
if ('serviceWorker' in navigator) {
  window.addEventListener('load', () => {
    navigator.serviceWorker.register('/sw.js').then((registration) => {
      console.log('‚úÖ Service Worker registrado exitosamente')
      
      // Escuchar actualizaciones
      registration.addEventListener('updatefound', () => {
        const newWorker = registration.installing
        if (newWorker) {
          newWorker.addEventListener('statechange', () => {
            if (newWorker.state === 'installed' && navigator.serviceWorker.controller) {
              console.log('üîÑ Nueva versi√≥n disponible')
              // Notificar al usuario sobre la actualizaci√≥n
              window.dispatchEvent(new Event('sw-update-available'))
            }
          })
        }
      })
    }).catch((error) => {
      console.warn('‚ö†Ô∏è Error registrando Service Worker:', error)
    })
  })
}

// Registrar Firebase Messaging Service Worker
if ('serviceWorker' in navigator) {
  navigator.serviceWorker.register('/LEGACY_FIREBASE-messaging-sw.js').catch((error) => {
    console.warn('‚ö†Ô∏è Firebase messaging SW error:', error)
  })
}

import ErrorBoundary from './components/common/ErrorBoundary'

ReactDOM.createRoot(document.getElementById('root')!).render(
  <React.StrictMode>
    <ErrorBoundary>
      <App />
    </ErrorBoundary>
  </React.StrictMode>,
)

